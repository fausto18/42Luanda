#include <unistd.h>

char	*ft_strcapitalize(char *str)
{
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z' && i == 0)
		{
			str[i] -= ('a' - 'A');
		}
		if (str[i] == ' ' || str[i] == '-' || str[i] == '+')
		{
			if (str[i + 1] >= 'a' && str[i + 1] <= 'z')
			{
				str[i + 1] -= ('a' - 'A');
			}
		}
		i++;
	}
	return (str);
}
