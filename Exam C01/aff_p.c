#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i] != '\0')
		{
			if (av[1][i] == 'p')
			{
				write(1, "p", 1);
				break ;
			}
			i++;
		}
		write(1, "\n", 1);
	}
	else
		write(1, "p\n", 2);
	return (0);
}
